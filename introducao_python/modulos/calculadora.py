# Função de adição
def somar(a, b):
    return a + b

# Função de subtração
def subtrair(a, b):
    return a - b

# Função de multiplicação
def multiplicar(a, b):
    return a * b

# Função de divisão
def dividir(a, b):
    if b == 0:
        return "Erro: Divisão por zero não é permitida."
    return a / b