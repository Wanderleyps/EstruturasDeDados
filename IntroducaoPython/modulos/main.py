# Importe as funções do módulo calculadora
from calculadora import somar, subtrair, multiplicar, dividir

resultado_soma = somar(5, 3)
resultado_subtracao = subtrair(10, 4)
resultado_multiplicacao = multiplicar(6, 7)
resultado_divisao = dividir(8, 2)

print("Soma:", resultado_soma)
print("Subtração:", resultado_subtracao)
print("Multiplicação:", resultado_multiplicacao)
print("Divisão:", resultado_divisao)
